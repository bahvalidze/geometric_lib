def area(a, b): 
    '''
    Returns area of ractangle

    Args:
        a: First side
        b: Second side

    Return:
        float: Rectangle's area
    
    Example:
        area(1, 1) # 1
    '''
    return float(a * b)

def perimeter(a, b):
    '''
    Returns perimeter of ractangle

    Args:
        a: First side
        b: Second side

    Return:
        float: Rectangle's perimeter

    Example:
        perimeter(1, 2) # 6
    ''' 
    return float(2 * (a + b))
