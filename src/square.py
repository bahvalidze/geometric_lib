
def area(a):
    '''
    Returns area of square

    Args:
        a: Side

    Return:
        float: Square's area

    Example:
        area(1) # 1
    '''
    return float(a * a)


def perimeter(a):
    '''
    Returns perimeter of square

    Args:
        a: Side

    Return:
        float: Square's perimeter

    Example:
        perimeter(1) # 4
    '''
    return float(a * 4)